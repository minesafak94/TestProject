package class7;

public class ForLoop {
    public static void main(String[] args) {
//print numbers from 0 to 9

        int number=0;//creating the variable of type int
        while (number<10){//checks the condition, is true execute the code in the body of while loop
            System.out.println(number);//prints the value of number on the console
            number++;// it adds one to the number variable
        }
      //for(initialize;condition;increment)
        for(int i=0;i<10;i++){
            System.out.println(i);
        }



        }

    }
